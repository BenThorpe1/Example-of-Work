#include "stdafx.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////
// init() - 
//////////////////////////////////////////////////////////////////////////////////////////////////////////
void cText::init(const char* meshName, int mesh_id, int shader_id, vec3 pos, vec3 rot_axis, float rot_angle, vec3 scale, int instance_count)
{
	this->m_instance_count = instance_count;
	this->cMesh::Init(meshName, mesh_id, shader_id, pos, rot_axis, rot_angle, scale);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
// render() - render a string of chars as a set of sprites
//////////////////////////////////////////////////////////////////////////////////////////////////////////
void cText::render(cShaderInfo* pShaderInfo, const char* pBuffer, bool bUseAlpha)
{
	this->cMesh::RenderSprite(pShaderInfo, pBuffer, bUseAlpha);
}