#include "stdafx.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////
// cTransform() - constructor
//////////////////////////////////////////////////////////////////////////////////////////////////////////
cTransform::cTransform()
{
	m_rotAngle	= 0.0f;
	m_rotAxis	= vec3(0.0f);
	m_pos		= vec3(0.0f);
	m_scale		= vec3(1.0f);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
// ~cTransform() - destructor
//////////////////////////////////////////////////////////////////////////////////////////////////////////
cTransform::~cTransform()
{
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
// init() - 
//////////////////////////////////////////////////////////////////////////////////////////////////////////
void cTransform::init(vec3 pos, vec3 rot_axis, float rot_angle, vec3 scale)
{
	m_pos		= pos;
	m_rotAxis	= rot_axis;
	m_rotAngle	= rot_angle;
	m_scale		= scale;
}