//////////////////////////////////////////////////////////////////////////////////////////////
// cNetworkManager() - constructor
//////////////////////////////////////////////////////////////////////////////////////////////
cNetworkManager::cNetworkManager()
{
	m_serverExists = false;
	m_serverSocket = -1;

	memset(m_accepted, false, sizeof(m_accepted));
}

//////////////////////////////////////////////////////////////////////////////////////////////
// ~cNetworkManager() - destructor
//////////////////////////////////////////////////////////////////////////////////////////////
cNetworkManager::~cNetworkManager()
{
}

//////////////////////////////////////////////////////////////////////////////////////////////
// add() - 
//////////////////////////////////////////////////////////////////////////////////////////////
void cNetworkManager::Add(bool a_startServer,const char* a_ipAddress,Scene* a_scene)
{
	if(a_startServer == true)
	{ 
		m_server.Init(a_scene);
		m_serverExists = true;
		m_smarties = 0;
	}
	else
	{
		m_client.Init(a_ipAddress, a_scene);

		m_smarties = 50;
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////
// update() - update this players count
//////////////////////////////////////////////////////////////////////////////////////////////
void cNetworkManager::Update()
{
	Sleep(1000);
	m_smarties++;
	if (m_smarties > 100)
	{
		m_smarties = 0;
	}
}

void cNetworkManager::Deinit()
{

}
